public class Switch {
    public static void main(String[] args) {
        int day = 7;
        switch (day) {
            case 5:
                System.out.println("je sobota");
                break;
            case 2:
                System.out.println("je neděle");
                break;
            default:
                System.out.println("Je pracovní týden");
                break;
        }
    }
}
